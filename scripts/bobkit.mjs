function compileCopilot(root, config) {
  const instructions = `# Bobkit Instructions

${GENERATED_HEADER}

This repository uses Bobkit, a portable workflow kit for AI coding agents.

## Working Rules

- Before planning, coding, debugging, reviewing, or declaring completion, check the relevant Bobkit skill.
- Start with \`skills/using-bobkit/SKILL.md\` when unsure.
- Use spec-first planning for new features and meaningful behavior changes.
- Use systematic debugging for failures.
- Use the verification gate before claiming work is complete.
- Project and user instructions remain higher priority than Bobkit.

## Skills

Project skills are mirrored into \`.github/skills/\` for GitHub Copilot. Use them when the task matches their descriptions.
`;

  writeText(root, '.github/copilot-instructions.md', instructions);
  copySkills(root, config.skillsDir, '.github/skills');
}

function copySkills(root, sourceDir, destDir) {
  const absSourceDir = path.join(root, sourceDir);
  const absDestDir = path.join(root, destDir);

  mkdirSync(absDestDir, { recursive: true });

  for (const skillName of readdirSync(absSourceDir).sort()) {
    const absSkillDir = path.join(absSourceDir, skillName);
    if (!statSync(absSkillDir).isDirectory()) {
      continue;
    }

    const sourceSkill = path.join(absSkillDir, 'SKILL.md');
    if (!existsSync(sourceSkill)) {
      continue;
    }

    const destSkillDir = path.join(absDestDir, skillName);
    mkdirSync(destSkillDir, { recursive: true });
    copyFileSync(sourceSkill, path.join(destSkillDir, 'SKILL.md'));
  }
}

function compileTarget(root, config, target) {
  switch (target) {
    case 'claude':
      compileClaude(root, config);
      break;
    case 'codex':
      compileCodex(root, config);
      break;
    case 'gemini':
      compileGemini(root, config);
      break;
    case 'opencode':
      compileOpenCode(root, config);
      break;
    case 'cursor':
      compileCursor(root, config);
      break;
    case 'copilot':
      compileCopilot(root, config);
      break;
    default:
      throw new Error(`Unknown compile target: ${target}`);
  }
}

function parseTarget(args) {
  const targetFlagIndex = args.indexOf('--target');
  if (targetFlagIndex !== -1) {
    return args[targetFlagIndex + 1] || 'all';
  }

  const inlineTarget = args.find((arg) => arg.startsWith('--target='));
  if (inlineTarget) {
    return inlineTarget.slice('--target='.length) || 'all';
  }

  return 'all';
}

function cleanTargetOutput(root, target) {
  if (target === 'copilot') {
    rmSync(path.join(root, '.github', 'skills'), { recursive: true, force: true });
  }
}

function compile(root, args) {
  const config = readJson(root, 'bobkit.json');
  const target = parseTarget(args);
  const validTargets = config.adapters.map((adapter) => adapter.id).sort();
  const targets = target === 'all' ? validTargets : [target];

  for (const requested of targets) {
    if (!adapterById(config, requested)) {
      throw new Error(`Unsupported target "${requested}". Expected one of: all, ${validTargets.join(', ')}`);
    }
  }

  for (const requested of targets) {
    cleanTargetOutput(root, requested);
    compileTarget(root, config, requested);
    console.log(`Compiled ${requested}`);
  }
}

function main() {
  const args = process.argv.slice(2);
  const command = args[0];

  if (!command || command === '--help' || command === '-h') {
    console.log(usage());
    return;
  }

  const root = findRoot(process.cwd());

  try {
    if (command === 'validate') {
      validate(root);
      return;
    }

    if (command === 'compile') {
      compile(root, args.slice(1));
      return;
    }

    console.error(usage());
    process.exitCode = 1;
  } catch (error) {
    console.error(`Bobkit ${command} failed: ${error.message}`);
    process.exitCode = 1;
  }
}

main();
